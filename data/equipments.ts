export function getEquipmentById(id: string): Equipment | undefined {
  return equipments.find((e) => e.id === id);
}

export function getEquipmentsByCategory(
  category: string
): Equipment[] {
  return equipments.filter((e) => e.category === category);
}

export function getFeaturedEquipments(count = 6): Equipment[] {
  // Prefer in-stock, refurbished+inspected machines
  return equipments
    .filter((e) => e.status === "in-stock" && e.refurbished && e.inspected)
    .slice(0, count);
}
